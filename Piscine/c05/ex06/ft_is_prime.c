int	ft_is_prime(int nb)
{
	int	a;
	int	result;

	result = 1;
	a = 2;
	if (nb <= 1)
		result = 0;
	while (a <= nb / 2)
	{
		if (nb % a == 0)
			result = 0;
		a++;
	}
	return (result);
}
